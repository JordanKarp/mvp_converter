function choose(choices) {
    let index = Math.floor(Math.random() * choices.length);
    return choices[index];
}

function mvp_convert(input_text) {
    let output = ''
    for (let char of input_text) {
        if (char in MVP) {
            let newChar = choose(MVP[char])
            output += newChar
        } else {
            output += char
        }
    }
    return output
}

